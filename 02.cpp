/**************************************************************
 Escreva um programa que calcule a média aritimética de dois
 números.
**************************************************************/
#include <iostream>

using namespace std;

int main()
{

    double n1, n2;

    cout << "Digite o primeiro numero: ";
    cin >> n1;

    cout << "Digite o segundo numero: ";
    cin >> n2;

    cout << (n1 + n2) / 2 << endl;
}